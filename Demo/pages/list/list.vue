<template>
	<view class="index">
		<block v-for="list in lists" :key="list.id">
			<view class="row">
				<view class="card card-list2" v-for="item in list.data" @click="goDetail(item)" :key="item.keys">
					<image class="card-img card-list2-img" :src="item.img_src"></image>
					<text class="card-num-view card-list2-num-view">{{item.img_num}}P</text>
					<view class="card-bottm row">
						<view class="car-title-view row">
							<text class="card-title card-list2-title">{{item.title}}</text>
						</view>
						<view @click.stop="share(item)" class="card-share-view"></view>
					</view>
				</view>
			</view>
		</block>
		<text class="loadMore">{{loadMoreText}}</text>
	</view>
</template>

<script>
	let keys = 0 ;
	export default {
		data() {
			return {
				refreshing: false,
				loadMoreText: '加载中...',
				lists: [],
				id: 0,
				fetchPageNum: 0
			}
		},
		onLoad(e) {
			uni.setNavigationBarTitle({
				title: '专题：' + e.type
			});
			console.log(e)
			this.id = e.id;
			// 防止app里由于渲染导致转场动画卡顿
			setTimeout(() => {
				this.getData();
			}, 300);
			uni.getProvider({
				service: 'share',
				success: (e) => {
					let data = [];
					for (let i = 0; i < e.provider.length; i++) {
						switch (e.provider[i]) {
							case 'weixin':
								data.push({
									name: '分享到微信好友',
									id: 'weixin'
								});
								data.push({
									name: '分享到微信朋友圈',
									id: 'weixin',
									type: 'WXSenceTimeline'
								});
								break;
							case 'qq':
								data.push({
									name: '分享到QQ',
									id: 'qq'
								});
								break;
							default:
								break;
						}
					}
					this.providerList = data;
				},
				fail: (e) => {
					console.log('获取分享通道失败', e);
				}
			});
		},
		onPullDownRefresh() {
			console.log('下拉刷新');
			this.refreshing = true;
			this.getData();
		},
		onReachBottom() {
			console.log('上拉加载刷新');
			if (this.fetchPageNum > 4) {
				this.loadMoreText = '没有更多了'
				return;
			}
			this.getData();
		},
		methods: {
			getData(e) {
				uni.request({
					url: this.$serverUrl + '/api/picture/list.php?type=' + this.id,
					success: (ret) => {
						if (ret.statusCode !== 200) {
							console.log('请求失败', ret)
						} else {
							if(ret.data.ret === 1001){
								console.log(ret.data.ret)
								this.loadMoreText = ret.data.msg
								return ;
							}
							if (this.refreshing && ret.data.data[0].id === (this.lists&&this.lists[0].data[0].id)) {
								uni.showToast({
									title: '已经最新',
									icon: 'none',
								});
								this.refreshing = false;
								uni.stopPullDownRefresh();
								return;
							}
							let list = {
									id: '',
									data: []
								},
								lists = [],
								data = ret.data.data;
							for (let i = 0, length = data.length; i < length; i++) {
								let index = Math.floor(i / 2);
									list.id = 'list' + keys;
								data[i].keys = keys++
								list.data.push(data[i]);
								if (i % 2 == 1) {
									lists.push(list);
									list = {
										id: '',
										data: []
									};
								}
							}
							console.log('list页面得到lists', lists);
							if (this.refreshing) {
								this.refreshing = false;
								uni.stopPullDownRefresh();
								this.lists = lists;
								this.fetchPageNum = 2;
							} else {
								this.lists = this.lists.concat(lists);
								this.fetchPageNum += 1;
							}
							this.fetchPageNum += 1;
						}
					}
				});
			},
			goDetail(e) {
				uni.navigateTo({
					url: '/pages/detail/detail?data=' + encodeURIComponent(JSON.stringify(e))
				});
			},
			share(e) {
				if (this.providerList.length === 0) {
					uni.showModal({
						title: '当前环境无分享渠道!',
						showCancel: false
					});
					return;
				}
				let itemList = this.providerList.map(function(value) {
					return value.name;
				});
				uni.showActionSheet({
					itemList: itemList,
					success: (res) => {
						uni.share({
							provider: this.providerList[res.tapIndex].id,
							scene: this.providerList[res.tapIndex].type && this.providerList[res.tapIndex].type === 'WXSenceTimeline' ?
								'WXSenceTimeline' : 'WXSceneSession',
							type: 0,
							title: 'uni-app模版',
							summary: e.title,
							imageUrl: e.img_src,
							href: 'https://uniapp.dcloud.io',
							success: (res) => {
								console.log('success:' + JSON.stringify(res));
							},
							fail: (e) => {
								uni.showModal({
									content: e.errMsg,
									showCancel: false
								});
							}
						});
					}
				});
			}
		}
	}
</script>

